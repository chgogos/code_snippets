num = int(input("Δώσε έναν ακέραιο αριθμό: "))

if num == 0:
    print("Ο αριθμός είναι μηδέν")
elif num > 0 and num % 2 == 0:
    print("Ο αριθμός είναι άρτιος και θετικός")
elif num < 0 and num % 2 == 0:
    print("Ο αριθμός είναι άρτιος και αρνητικός")
elif num > 0 and num % 2 != 0:
    print("Ο αριθμός είναι περιττός και θετικός")
else:
    print("Ο αριθμός είναι περιττός και αρνητικός")
